#2. Celsius to Fahrenheit Temperature Converter
#Write a program that converts Celsius temperatures to Fahrenheit temperatures. The 
#formula is as follows:
#              F = ((5//9)*C) + 32


#1. input (receive from user)
C = int(input("Enter temperature in Celcius: "))
print("")

#2. process (calculate and therefore convert)
F = (0.5*C) + 32

#3. output (display the result)
print("Processing...")
print(f"Temperature in Fahrenheit: {F}")

